 public class Box {
    double mass = 0;
    double size = 0;
    double fr = 0;
    double posX = 0;
    double posY = 0;

    public Box(double mass, double size, double fr, double posX, double posY){
        this.mass = mass;
        this.size = size;
        this.fr = fr;
        this.posX = posX;
        this.posY = posY;
    }
}

